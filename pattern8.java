class pattern8
{
  public static void main(String args[])
 {
   int n=5;
	for(int i=1;i<=n;i++)
	{  
          for(int k=n;k>=1;k--)
          {
              
            for(int j=0;j<=n-i;j++)
		{      
                 System.out.print(k + " ");
            }
           
          }
          System.out.print("\n");
			
	}
       System.out.print("\n");
 }
}
      